/*Write a program that takes as input two numbers and a string denoting the operation (“+”, “-
    ”, “*”, “/”) and prints out the appropriate result. Watch out for division by zero!*/

var a = 2
var b = 3
var result
switch (result) {
    case a + b:
        console.log(result)

        break;
    case a - b:
        console.log(result)
        break;
    case a * b:
        console.log(result)
        break;
    case a / b:
        console.log(result)

    default:
        break;
}